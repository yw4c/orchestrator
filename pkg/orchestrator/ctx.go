func (c *Context) setPbReq(req interface{}) {
	c.Set(pbReqCtxKey, req)
}

func (c *Context) LoadPbReq(pbReq interface{}) {
	req, exist := c.Get(pbReqCtxKey)
	if exist {
		helper.StructAssign(pbReq, req)
	}
}

